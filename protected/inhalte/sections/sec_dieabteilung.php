<h1>Informationen zur Abteilung Badminton</h1>

<p>Wir schreiben das Jahr 1995: Thomas Friedrich und einige Federballbegeisterte treffen sich zum
regelmäßigen Spiel in der PKG-Halle. Die Idee der Gründung einer Sportgruppe innerhalb des TSV 
Gersthofen (<a href="http://www.tsv-gersthofen.de"
<?php echo STD_NEW_WINDOW ?>>www.tsv-gersthofen.de</a>) war geboren. Badminton wird als Sparte innerhalb der 
Abteilung Leichtathletik gegründet. Der Großteil der Gründungsmitglieder ist auch heute noch dabei. 
Nach einjährigem Training nahmen die Spielerinnen und Spieler erstmals 1996 am Punktspielbetrieb 
teil. Zur gleichen Zeit kamen eine Schüler- und eine Jugendgruppe dazu.</p>

<p>Sportlich ging es stetig bergauf: 1998 Aufstieg in die B-Klasse, 2002 in die A-Klasse, 2007 in die 
Bezirksoberliga. Fortwährend sind mindesten 3 Mannschaften im Punktspielbetrieb aktiv. Der 
sportliche Erfolg ist gewährleistet durch engagiertes und regelmäßiges Training, Integration aller 
Mitglieder und nicht zuletzt zahlreiche gesellige Veranstaltungen.</p>

<p>Als besonderes Jahr wird 2016 in die Annalen eingehen: Am 24.1.2016, knapp 20 Jahre nach der 
Abteilungsgründung, beschließen die Mitglieder die Gründung einer eigenen Abteilung im TSV 
Gersthofen. Durch den Beschluss des Vereinsrats am 3.3.2016 entsteht die 15. Abteilung im Verein.</p>

<p>Allen Interessenten und Neulingen empfehlen wir außerdem die Lektüre der Unterrubriken. Sie 
geben einen guten Einblick in die Abteilung Badminton. Hier findet ihr unsere Ziele und lernt wie die 
Abteilung aufgebaut ist. In der Bildergalerie sind Aufnahmen und Berichte aller bisher 
stattgefundenen Events zu finden.</p>