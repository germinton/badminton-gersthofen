<h1>Spielbetrieb</h1>

<p>Die Abteilung Badminton bietet allen Athleten die Möglichkeit die im
wöchentlichen <a href="index.php?section=sportstaetten">Regel-Training</a>
erlernten Fähigkeiten im Wettkampf anzuwenden. Neben <a
	href="index.php?section=termine_allg">Turnieren in der Region</a>,
bietet sich hierfür vor allem die Teilnahme am Punktspielbetrieb an.
<p class="textbox papier">Wieso ist eine Teilnahme am Punktspielbetrieb
sinnvoll?</p>

<ul>
	<li>Erfahrung im Wettkampf führt zu steilerer Lernkurve</li>
	<li>Möglichkeit sich mit anderen zu messen</li>
	<li>Wettkampf fördert die Motivation besser zu werden</li>
	<li>Regel-Training erfährt deutliche Aufwertung</li>
	<li>Verantwortungsbewusstsein wird gefördert und gefordert</li>
	<li>Spaß durch geselliges Miteinander und Team-Geist</li>
</ul>

<br />

<p class="textbox papier">Wie funktioniert der Punktspielbetrieb? (siehe
unten: Abweichungen Schüler/Jugend)</p>

<ul>
	<li>Badminton ist Mannschaftssport: Mannschaften verschiedener Vereine
	treten gegeneinander an</li>
	<li>Aktive (&gt;18 Jahre): Saisondauer von September-April, aufgeteilt
	in eine Hin- und Rückrunde<br />
	<img src="bilder/saisonablauf_aktive.jpg"
		alt="Saisonablauf der Aktiven" height="90" /></li>
	<li>Gespielt wird in verschiedenen Leistungsklassen<br />
	<img src="bilder/ligen.jpg" alt="Einteilung der Ligen" height="120" /></li>
	<li>Eine Mannschaft besteht aus mindestens 4 Herren und 2 Damen</li>
	<li>Für einen gewonnenen Spieltag erhält der Verein 2 Punkte, für ein
	Unentschieden 1 Punkt</li>
	<li>Am Ende der Saison werden anhand der Siegpunkte Aufsteiger und
	Absteiger bestimmt</li>
</ul>

<br />

<p class="textbox papier">Wie läuft ein Punktspieltag ab?</p>

<ul>
	<li>An einem Punktspieltag wird gegen zwei gegnerische Mannschaften
	gespielt</li>
	<li>Punktspieltage an einem Samstag sind von 14-18Uhr, am Sonntag von
	10-14Uhr</li>
	<li>Die gegnerischen Teams befinden sich in einem Radius von ca. 50km
	um Augsburg</li>
	<li>Es werden gegen jede gegnerische Mannschaft folgende 8 Spiele
	ausgetragen (Dauer ca. 2h)<br />
	<img src="bilder/teams.jpg" alt="Ausgetragene Begegnungen" height="180" /></li>
	<li>Mögliche Spieltagergebnisse sind z.B. 4:4, 1:7 oder 0:8</li>
</ul>

<br />

<p class="textbox papier">Welche Anforderungen und Verpflichtungen
ergeben sich für einen Mannschaftsspieler?</p>

<ul>
	<li>Zuverlässige Teilnahme an Punktspielterminen</li>
	<li>Regelmäßige Trainingsteilnahme</li>
	<li>Team-Spirit</li>
	<li>Jede Mannschaft stellt einen Mannschaftsführer</li>

</ul>

<br />

<p><em>Wichtig: Die Teilnahme an Punktspielen sollte nur ausnahmsweise
in Notfällen abgesagt werden. Ein Spielerausfall führt zur kampflosen
Abgabe von Spieltagen und im Wiederholungsfall zur Sperrung des gesamten
Teams. Dies ist nicht nur sehr frustrierend für das Team, sondern zieht
auch eine Strafzahlung für die Abteilung nach sich.</em></p>

<br />

<p class="textbox papier">Welche Aufgaben hat der Mannschaftsführer?</p>

<ul>
	<li>Verfügbarkeit/Teilnahme der Team-Kollegen abfragen</li>
	<li>Im Notfall: Organisation von Ersatz-Spielern</li>
	<li>Dokumentation der Spielergebnisse</li>
	<li>Festlegung der Meldereihenfolge zur Hin- und Rückrunde</li>
	<li>Bei Heimspielen: Netz, Hallenschlüssel, Catering, Bälle
	organisieren</li>
</ul>

<br />

<p class="textbox papier">Welche Besonderheiten gibt es bei der
Punktrunde für Schüler und Jugendliche zu beachten?</p>


<ul>
	<li>Schüler/Jugend (&lt;18 Jahre): Saisondauer von September-Januar<br />
	<img src="bilder/saisonablauf_jugend.jpg"
		alt="Saisonablauf der Schüler/Jugendlichen" height="90" /></li>

	<li>Eltern stehen (abwechselnd) für den Fahrdienst zur Verfügung</li>
	<li>Jugendwart (Schüler)/Mannschaftsführer (Jugend) organisieren den
	Fahrdienst per E-Mail</li>
	<li>Eltern helfen mit die Anwesenheit der Sportler an den
	Punktspielterminen sicherzustellen</li>
	<li>Zuschauen erlaubt: Eltern als Fans sind jederzeit auf den
	Punktspielen willkommen</li>
</ul>

