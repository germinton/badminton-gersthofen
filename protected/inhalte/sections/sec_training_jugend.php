<h1>Jugend-Training</h1>

<img src="bilder/training_jugend.jpg" alt="Jugend-Training" />

<p>Viele unserer <strong>Jugendlichen im Alter zwischen 13 und 18 Jahren</strong>haben mehrere Sportarten
ausprobiert und entdecken dabei den Reiz des Badminton-Sports: Spritzigkeit, schnelle 
Reaktionsfähigkeit und Köpfchen sind gefragt, wenn man ein Badmintonspiel gewinnen 
möchte. Die Sportler lernen daher im Training neben den Grundtechniken des Laufens und 
Schlagens auch taktische Strategien kennen ein Spiel zu gewinnen.</p>

<p>In den Punktspielen haben leistungsstarke Jugendlichen die Möglichkeit sich mit 
Altersgenossen zu messen. Dabei entwickeln sie Teamgeist und Verantwortungsbewusstsein 
für Ihre Mannschaft.</p>

<p>&nbsp;</p>
<p>Ablauf des Trainings:</p>
<ul>
	<li>30min: gemeinsames Aufwärmen</li>
	<li>30min: angeleitetes Training</li>
	<li>30min: freies Spiel zum Ausprobieren des Erlernten</li>
</ul>

