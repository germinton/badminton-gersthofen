<p class="textbox papier">Wie läuft ein typisches Training ab?</p>
<p>Alle Sportler sollen durch angeleitetes Training zum Badmintonsport hingeführt werden. Technik- und Taktik-Training
sowie zahllose Sportspiele sorgen für ein abwechslungsreiches und kurzweiliges Training. Es besteht die Möglichkeit, an
Wettkämpfen teilzunehmen, dies ist aber kein Muss.</p>

<ul>
	<li><em>30min:</em> gemeinsames Aufwärmen</li>
	<li><em>60min:</em> gemeinsames Training. Es werden neben den Grundschlägen und Lauftechniken auch Taktik und
	Badmintonregeln erlernt.</li>
	<li><em>90min:</em> freies Spiel zum Ausprobieren des Erlernten. Je nach Bedarf werden auch zusätzliche
	Trainingseinheiten für einzelne Spieler angeboten. Diese erfolgen in Absprache mit dem jeweiligen Trainer.</li>
</ul>

