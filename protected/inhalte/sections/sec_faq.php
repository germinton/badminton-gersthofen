<h1><abbr title="Frequently asked questions">FAQ</abbr></h1>

<p>Die folgende Übersicht zeigt die meist gestellten Fragen unserer neuen Mitglieder. Sollten
nach der Durchsicht noch Fragen offen sein, scheue Dich nicht diese an uns zu richten und 
schreibe uns bitte eine E-Mail an: <a href="mailto:info@badminton-gersthofen.de?subject=Frage zur Abteilung Badminton">info@badminton-gersthofen.de</a>
<p>
<p class="textbox papier">
Die E-Mail geht automatisch an einen ausgewählten Kreis innerhalb der Abteilung. Du
erreichst damit maximale Aufmerksamkeit bzw. Deine Frage wird zuverlässig beantwortet.</p>

<h2>Fragen zum Training</h2>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Ich bin noch kein Mitglied im TSV Gersthofen. Kann ich zum Probetraining kommen?</p>
	<p>Du kannst bis zu 5 Probetrainings mitmachen, damit Du uns auf Herz und Nieren testen
	kannst. Auch in dieser Zeit bist Du bei Verletzungen durch den Verein versichert. Ab dem 5. 
	Training musst Du Mitglied im TSV Gersthofen sein.</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Was soll ich zum ersten Training mitbringen?</p>
	<p>Zum ersten Trainingsbesuch bringst Du Sportsachen (T-Shirt, kurze Hose, Turnschuhe mit 
	heller Sohle) mit, in denen Du Dich gut bewegen kannst und wohl fühlst. In allen Sporthallen 
	sind Duschen vorhanden. Wenn Du bereits einen Schläger besitzt, bring ihn mit. Bälle haben 
	wir.</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Ich möchte zum Probetraining kommen. Muss ich mich vorher anmelden?</p>
	<p>Du kannst jederzeit zu unseren Regel-Trainings hinzustoßen. Idealerweise gibst Du uns vorher
	per E-Mail an <a href="mailto:info@badminton-gersthofen.de?subject=Frage zur Abteilung Badminton">info@badminton-gersthofen.de</a>
	Bescheid. Bitte prüfe auf unserer Webseite, ob 
	Trainingsausfälle verzeichnet sind, bevor Du kommst.</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Ich bin kein Jugendlicher mehr und möchte Badminton spielen lernen. Hat es Sinn, dass ich
	Badminton im Verein spiele?</p>
	<p>Es ist noch kein Meister vom Himmel gefallen. So ist jeder unserer aktiven Spieler auch
	einmal ein Anfänger gewesen. Anfänger sind bei uns herzlich willkommen, egal in welchem 
	Alter sie den Sport erlernen. Wir integrieren neue Spieler in das Training und die Erfahrung 
	lehrt, dass die meisten in den ersten Wochen große Fortschritte machen.</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Ich bin Anfänger und habe noch keinen Schläger. Kann ich vorerst einen Schläger 
	ausleihen?</p>
	<p>Wir haben Leihschläger, die Du gerne für die ersten Male nutzen kannst. Bei Deinem ersten 
	Schlägerkauf beraten wir Dich gerne!</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Ich bin Anfänger und habe Sorge, dass meine Kondition für ein 3-stündiges Training nicht 
	ausreicht.</p>
	<p>Wir trainieren nicht 3 Stunden am Stück durch. Falls Du vorher keinen Sport gemacht hast, 
	wird Dir sicherlich zu Beginn die Luft wegbleiben. Schon nach wenigen Trainings gewöhnt sich 
	der Körper an die Belastung und Du wirst die Fortschritte merken. Mangelnde Kondition ist 
	also kein Problem, man kann schließlich jederzeit eine Pause machen.</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Ich suche eine Gelegenheit zum Sport treiben, möchte aber in keiner Mannschaft sein und
	Punktspiele bestreiten. Kann ich trotzdem ins Training kommen?</p>
	<p>Es freut uns natürlich wenn ambitionierte Spieler es sich zum Ziel setzen im
	Wettkampfbetrieb mitzumachen. Das ist aber keine Verpflichtung und so ist uns auch jeder 
	Sportler, der keine Wettkampfabsichten hegt, willkommen.</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Fällt das Training während der Schulferien aus?</p>
	<p>Da es sich bei den von uns genutzten Turnhallen um Schul-Turnhallen handelt, hängt die 
	Hallennutzung von den Schulferien ab. Ziel ist es durch Ersatz-Trainingstermine einen 
	durchgehenden Trainingsbetrieb sicherzustellen. Alle Änderungen werden über unsere 
	Webseite kommuniziert.</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Wie erfahre ich von Änderungen im Trainingsbetrieb?</p>
	<p>Unsere Webseite informiert immer aktuell über Änderungen im Trainingsbetrieb. Es
	empfiehlt sich daher vor dem Trainingsbesuch die Webseite auf Neuigkeiten hin zu prüfen.</p>
</div>


