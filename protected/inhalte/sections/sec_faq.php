<h2>Allgemeine Fragen</h2>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Kann ich auch nur vorübergehend Badminton im TSV Gersthofen spielen? Welche Kosten 
	fallen dann an?</p>
	<p>Voraussetzung für Deine regelmäßige Teilnahme im Badminton-Training ist die 
	Mitgliedschaft im TSV Gersthofen sowie in der Abteilung Badminton. Der Eintritt in den 
	Verein ist jederzeit möglich, der Austritt zum Ende des Geschäftsjahres. Ein- und Austritt in 
	die Abteilung Badminton ist jederzeit möglich. Die Berechnung der Vereinsmitgliedschaft und 
	des Abteilungsbeitrags erfolgen monatsgenau.
	</p>
	<p>
	Beispiel: Du bist Student und nur zum Sommersemester (April-September) in Augsburg, 
	möchtest aber in dieser Zeit Badminton bei uns spielen. Du bezahlst den Vereinsbeitrag von 
	April – Dezember und den Abteilungsbeitrag von April-September.</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Wird mit Plastik-oder Naturfederbällen gespielt?</p>
	<p>Da Plastikbälle unkontrolliert und viel zu schnell fliegen, spielt der Badmintonspieler mit
	einem echten Federball. Plastikbälle sind für's Federballspiel im Freibad OK, aber Badminton 
	macht mit Federbällen mehr Spaß!</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Müssen Bälle selbst mitgebracht werden?</p>
	<p>Nein. Naturfederbälle stehen allen Spielern im Training zur Verfügung. Hierfür wird ein
	Abteilungsbeitrag erhoben. Damit ergibt sich wegen der größeren Einkaufsmenge für alle ein 
	Kostenvorteil, zudem sind immer Bälle gleicher Qualität vorhanden.</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Welche Rechte und Pflichten entstehen mir durch die Mitgliedschaft im Verein bzw. in der 
	Abteilung?</p>
	<p>Auf der Soll-Seite stehen die Zahlung der Mitgliedschaft im Verein sowie des 
	Abteilungsbeitrags. Auf der Haben-Seite bekommst Du die Nutzung unserer Sportstätten, ein 
	angeleitetes Training, bist bei Sportverletzungen versichert, kannst Badminton 
	wettkampfmäßig betreiben und gewinnst natürlich Kontakt zu netten Leuten, die die 
	Begeisterung für den Badmintonsport mit Dir teilen. Das, was das Vereinsleben wirklich 
	besonders macht, sind übrigens gerade die sozialen Kontakte und die gemeinsamen 
	Unternehmungen, die neben dem Sport organisiert werden. Vielleicht schenkst Du, wenn Du 
	nach einiger Zeit auf den Geschmack gekommen bist, dieser Gemeinschaft etwas von Deiner 
	wertvollen Zeit, indem Du ein kleineres Ehrenamt übernimmst.</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Ich werde nur sporadisch Badminton spielen. Muss ich ebenfalls den Abteilungsbeitrag 
	bezahlen?</p>
	<p>Wir engagieren uns ehrenamtlich und halten daher Prozesse und interne Aufwände schlank.
	Aus diesem Grund können wir keine Sonderwege beim Einzug des Abteilungsbeitrags 
	zulassen.</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Gibt es andere Bezahlmöglichkeiten als die SEPA Lastschrift, wie zum Beispiel Überweisung
	oder Barzahlung?</p>
	<p>Wir engagieren uns ehrenamtlich und halten daher Prozesse und interne Aufwände schlank.
	Aus diesem Grund können wir keine Sonderwege beim Einzug des Abteilungsbeitrags 
	zulassen.</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Bekomme ich den Abteilungsbeitrag erstattet, wenn ich nicht am Training teilnehme?</p>
	<p>Der Abteilungsbeitrag basiert auf einer Mischkalkulation und soll nicht den persönlichen 
	Ballverbrauch 1:1 abdecken. Je öfter Du also kommst, umso günstiger wird für Dich der Sport 
	in der Abteilung Badminton. Eine Erstattung von Abteilungsbeiträgen ist daher nicht möglich.</p>
</div>
