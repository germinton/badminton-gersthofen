<h1><abbr title="Frequently asked questions">FAQ</abbr></h1>

<p>Die folgende Übersicht zeigt die meist gestellten Fragen unserer neuen Mitglieder zum Trainingsbetrieb. Sollten nach
der Durchsicht noch Fragen offen sein, scheue Dich nicht diese an uns zu richten und schreibe uns bitte eine E-Mail an <a
	href="mailto:webmaster@badminton-gersthofen.de?subject=Frage zur Abteilung Badminton">webmaster@badminton-gersthofen.de</a>.</p>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Ich bin Anfänger. Macht es Sinn, dass ich Badminton im Verein spiele?</p>
	<p>Es ist noch kein Meister vom Himmel gefallen. So ist jeder unserer aktiven Spieler auch ein mal ein Anfänger gewesen.
	Anfänger sind bei uns herzlich willkommen und werden so gut es geht in das Training integriert.</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Ich bin Anfänger und habe noch keinen Schläger. Kann ich vorerst einen Schläger ausleihen?</p>
	<p>Wir haben Leihschläger, die Du gerne für die ersten Male nutzen kannst. Bei Deinem ersten Schlägerkauf beraten wir Dich gerne!</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Ich bin Anfänger und habe Sorge, dass meine Kondition für ein 3-stündiges Training nicht ausreicht.</p>
	<p>Wir trainieren nicht 3 Stunden am Stück durch. Falls Du vorher keinen Sport gemacht hast, wird Dir sicherlich zu
	Beginn die Luft wegbleiben. Schon nach wenigen Trainings gewöhnt sich der Körper an die Belastung und Du wirst die
	Fortschritte merken. Mangelnde Kondition ist also kein Problem, man kann schließlich jederzeit eine Pause machen.</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Wird mit Plastik-oder Naturfederbällen gespielt?</p>
	<p>Da Plastikbälle unkontrolliert und viel zu schnell fliegen, spielt der Badmintonspieler mit einem echten Federball.
	Plastikbälle sind für's Federballspiel im Freibad OK, aber Badminton macht mit Federbällen mehr Spaß!</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Müssen Bälle selbst mitgebracht werden?</p>
	<p>Nein. Naturfederbälle stehen allen Spielern im Training zur Verfügung. Diese werden von einem vierteljährlich zu
	zahlenden Ballgeld besorgt. Damit ergibt sich wegen der größeren Einkaufsmenge für alle ein Kostenvorteil, zudem sind
	immer Bälle vorhanden.</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Ich suche eine Gelegenheit zum Sport treiben, möchte aber in keiner Mannschaft sein und
	Punktspiele bestreiten. Kann ich trotzdem ins Training kommen?</p>
	<p>Es freut uns natürlich wenn ambitionierte Spieler es sich zum Ziel setzen im Wettkampfbetrieb mitzumachen. Das ist
	aber keine Verpflichtung und so ist uns auch jeder Sportler, der keine Wettkampfabsichten hegt, willkommen.</p>
</div>

<div class="collapse" tabindex="1">
	<p class="textbox papier klickbar">Welche Rechte und Pflichten entstehen mir durch die Mitgliedschaft im Verein bzw. in dieser
	Abteilung?</p>
	<p>Auf der Soll-Seite stehen die Zahlung der Mitgliedschaft im Verein sowie unseren Abteilungsbeitrag. Auf
	der Haben-Seite bekommst Du die Nutzung unserer Sportstätten, ein angeleitetes Training, bist bei Sportverletzungen
	versichert, kannst Badminton wettkampfmäßig betreiben und gewinnst natürlich Kontakt zu netten Leuten, die die
	Begeisterung für den Badmintonsport mit Dir teilen. Das, was das Vereinsleben wirklich besonders macht, sind übrigens
	gerade die sozialen Kontakte und die gemeinsamen Unternehmungen, die neben dem Sport organisiert werden. Vielleicht
	schenkst Du, wenn Du nach einiger Zeit auf den Geschmack gekommen bist, dieser Gemeinschaft etwas von Deiner wertvollen
	Zeit, indem Du ein kleineres Ehrenamt übernimmst.</p>
</div>