<h1>Gebühren</h1>

<p><h2>Was kostet mich Badminton spielen im TSV Gersthofen?</h2></p>

<p>Die Gesamtkosten setzen sich aus dem <strong>Mitgliedsbeitrag des Vereins</strong>
und dem <strong>Beitrag der Abteilung</strong> zusammen. Beide Beträge werden per Lastschrift eingezogen.</p>

<p><strong>Mitgliedsbeitrag:</strong> Dieser Beitrag deckt die Verwaltung,
Trainer und Hallenmiete und berechtigt zur Teilnahme an Übungsstunden aller
Abteilungen. Dieser Beitrag wird halbjährig durch den Verein eingezogen.</p>

<p><strong>Abteilungsbeitrag:</strong> Dieser Beitrag fließt der Abteilung Badminton zu. Er wird vom Verein zusammen mit dem Mitgliedsbeitrag eingezogen.
Wir spielen mit Natur- Federbällen aus Gänsefedern. Um einen reibungslosen Trainingsbetrieb sicherzustellen, werden die Federbälle zentral von der Abteilung Badminton
besorgt und im Training zur Verfügung gestellt. Von den Qualitäts- und Kostenvorteilen profitieren alle Sportler. Aus diesem Grund erhebt die Abteilung Badminton
zusätzlich einen altersabhängigen Abteilungsbeitrag.</p>

